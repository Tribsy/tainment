def get_fish_count() -> int:
    return sum(len(v) for v in FISH.values())


def get_tier_for_fish(fish_name: str) -> str | None:
    for tier, fish_list in FISH.items():
        for f in fish_list:
            if f[0] == fish_name:
                return tier
    return None


def can_catch_with_rod(fish_min_rod: int, user_rod_tier: int) -> bool:
    return user_rod_tier >= fish_min_rod


def get_catchable_tiers(rod_tier: int) -> list[str]:
    return [t for t, meta in TIERS.items() if rod_tier >= meta['min_rod']]


def get_rod_info(rod_key: str) -> dict | None:
    return RODS.get(rod_key)


def get_tier_weight_table(rod_tier: int) -> list[tuple[str, float]]:
    """Return (tier_name, weight) pairs for a given rod tier, for weighted random selection."""
    return [(t, meta['weight']) for t, meta in TIERS.items() if rod_tier >= meta['min_rod']]
